package estructurasDeDatos;

import java.util.HashSet;

public class ECR {
	String etiqueta;
	HashSet<ConjuntoRasgos> conjuntosRasgos;
	
	
	public ECR(){
		conjuntosRasgos = new HashSet<ConjuntoRasgos>();
	}
	
	public String getEtiqueta() {
		return etiqueta;
	}
	public void setEtiqueta(String etiqueta) {
		this.etiqueta = etiqueta;
	}
	public HashSet<ConjuntoRasgos> getConjuntosRasgos() {
		return conjuntosRasgos;
	}
	public void setConjuntosRasgos(HashSet<ConjuntoRasgos> rasgos) {
		this.conjuntosRasgos = rasgos;
	}
}
